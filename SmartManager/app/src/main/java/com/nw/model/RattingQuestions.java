package com.nw.model;

public class RattingQuestions
{
	private int Value;
	private String name;
	private int Total;

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public int getValue()
	{
		return Value;
	}

	public void setValue(int value)
	{
		Value = value;
	}

	public int getTotal()
	{
		return Total;
	}

	public void setTotal(int total)
	{
		Total = total;
	}

}
